txt = "this is a test sentence"

print(txt.swapcase())           # swapcase

print(txt.find("a"))            # find, has found the value at 8
print(txt.find("w"))            # couldn't find "w" , so returned -1




print(txt.index("a"))           # index, has found the value at 8
print(txt.index("w"))            # couldn't find "w" , so returned an error message
